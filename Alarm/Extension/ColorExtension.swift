import Foundation
import SwiftUI

extension Color {
    static let backgroundColor1 = Color("black")
    static let backgroundColor2 = Color("gray1")
    static let backgroundColor3 = Color("gray2")
    
    static let fontColor1 = Color("orange")
    static let fontColor2 = Color("gray3")
    static let fontColor3 = Color("gray4")
}

extension UIColor {
    static let backgroundColor1 = UIColor(named: "black")
    static let backgroundColor2 = UIColor(named: "gray1")
    static let backgroundColor3 = UIColor(named: "gray2")
    
    static let fontColor1 = UIColor(named: "orange")
    static let fontColor2 = UIColor(named: "gray3")
    static let fontColor3 = UIColor(named: "gray4")
}
