import Foundation
import SwiftUI

extension Color {
    static let backgroundColor = Color("black")
    static let backgroundColor2 = Color("gray1")
    static let backgroundColor3 = Color("gray2")
    
    static let primaryColor = Color("gray3")
    static let secondaryColor = Color("orange")
}

extension UIColor {
    static let backgroundColor = UIColor(named: "black")
    static let backgroundColor2 = UIColor(named: "gray1")
    static let backgroundColor3 = UIColor(named: "gray2")
    
    static let primaryColor = UIColor(named: "gray3")
    static let secondaryColor = UIColor(named: "orange")
}
